package com.example.aidanci.Entity;

/**
 * 信息实体类
 */
public class Info {
    public String totalwords;
    public String havewords;
    public String mycount;

    public String getTotalwords() {
        return totalwords;
    }

    public void setTotalwords(String totalwords) {
        this.totalwords = totalwords;
    }

    public String getHavewords() {
        return havewords;
    }

    public void setHavewords(String havewords) {
        this.havewords = havewords;
    }

    public String getMycount() {
        return mycount;
    }

    public void setMycount(String mycount) {
        this.mycount = mycount;
    }
}
